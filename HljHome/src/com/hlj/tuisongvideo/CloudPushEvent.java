package com.hlj.tuisongvideo;

public class CloudPushEvent {
	public int id;
	public long position;
	public String url = "";
	public String session_id;

	@Override
	public String toString() {
		return "{name: " + url + ", position: " + position + ",id: " + id
				+ ", session_id: " + session_id + "}";
	}
}
